# بنبدأ بكتابة مصفوفة فيها شوية أرقام عشوائية
arr = [64, 25, 12, 22, 11]  

# بنحسب عدد العناصر اللي في المصفوفة
n = len(arr)

# اللوب الخارجي بيمشي على كل عنصر0
for i in range(n - 1):
    # بنفترض إن أول عنصر هو الأصغر
    min_index = i

    # اللوب الداخلي بيدور على العنصر الأصغر فعلاً
    for j in range(i + 1, n):
        if arr[j] < arr[min_index]:  # لو لقينا رقم أصغر
            min_index = j  # نحفظ مكانه

    # بنبدّل بين العنصر الأصغر والعنصر الحالي
    arr[i], arr[min_index] = arr[min_index], arr[i]

# في الآخر بنطبع المصفوفة بعد الترتيب
print("Sorted array:", arr)
